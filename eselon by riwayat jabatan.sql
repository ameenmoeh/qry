SELECT A.NIP, C.NIP_BARU, C.NAMA, C.AGAMA, IIF(C.JENIS_KELAMIN=0,'Perempuan','Laki-Laki') AS JENIS_KELAMIN, 
A.KETERANGAN, A.TMT_SK, A.TGL_SK, C.JENJANG_PENDIDIKAN, C.PANGKAT, C.GOL_RUANG, C.KET_JABATAN, c.TMT_PENSIUN,
B.JABATAN, B.KODE_JABATAN, B.KODE_LEVEL_JABATAN 
FROM TR_JABATAN A 
LEFT JOIN TM_JABATAN B ON B.KODE_JABATAN = A.KODE_JABATAN
LEFT JOIN vwDATA_PEGAWAI C ON C.NIP = A.NIP
WHERE A.KODE_JABATAN IN ('00101','00102','00103','00104','00105','00106','00301','00302','00303','00304','00370','00403')
AND A.KETERANGAN LIKE '%RADEN FATAH%'
ORDER BY A.TMT_SK

