SELECT --TOP 100
    A.NIP_BARU, A.NAMA_LENGKAP, A.PANGKAT, A.GOL_RUANG, A.JABATAN, A.KET_JABATAN, f.LEVEL_JABATAN,
	G.NO_HP, G.EMAIL, a.KODE_SATUAN_KERJA,
	E.SATUAN_KERJA AS SATKER1, D.SATUAN_KERJA AS SATKER2, A.SATUAN_KERJA
FROM vwDATA_PEGAWAI A 
LEFT JOIN TM_SATUAN_KERJA d ON d.KODE_SATUAN_KERJA=LEFT(a.KODE_SATUAN_KERJA,6)+'00000000' 
LEFT JOIN TM_SATUAN_KERJA e ON e.KODE_SATUAN_KERJA=LEFT(a.KODE_SATUAN_KERJA,4)+'0000000000'
LEFT JOIN TM_LEVEL_JABATAN f on f.KODE_LEVEL_JABATAN = a.KODE_LEVEL_JABATAN
LEFT JOIN TD_ALAMAT G ON G.NIP = A.NIP
LEFT JOIN TR_SKP H ON H.NIP = A.NIP AND H.TAHUN='2021'
WHERE A.KODE_MUTASI=1  
    --AND F.KODE_TIPE_JABATAN=0 
    --AND F.KODE_LEVEL_JABATAN IN ('001','003','005', '701')
    --AND A.KODE_SATUAN_KERJA LIKE '0102%'
    AND A.KODE_JABATAN IN ('00101','00102','00103','00104', '00305','20213','20217','20222','20312','20313','20441','20443','20444')
order by a.KODE_SATUAN_KERJA